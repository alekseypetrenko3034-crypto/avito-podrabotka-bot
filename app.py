# -------------------------- ФУНКЦИЯ ЗАПРОСА К YANDEXGPT --------------------------
def ask_yandexgpt(messages, temperature=0.3):
    """Отправляет запрос в YandexGPT и возвращает ответ."""
    payload = {
        "modelUri": f"gpt://{YANDEX_FOLDER_ID}/yandexgpt/latest",
        "completionOptions": {
            "stream": False,
            "temperature": temperature,
            "maxTokens": "2000"
        },
        "messages": messages
    }
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Api-Key {YANDEX_API_KEY}",
        "x-folder-id": YANDEX_FOLDER_ID
    }
    try:
        resp = requests.post(YANDEXGPT_URL, json=payload, headers=headers)
        resp.raise_for_status()
        data = resp.json()
        return data["result"]["alternatives"][0]["message"]["text"]
    except Exception as e:
        logger.error(f"YandexGPT error: {e}")
        return "Извините, произошла ошибка. Повторите, пожалуйста, ваш запрос или свяжитесь с логистом."

