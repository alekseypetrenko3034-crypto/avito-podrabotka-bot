@app.route("/avito-webhook", methods=["POST"])
def avito_webhook():
    # Проверка challenge-запроса при регистрации вебхука
    if request.json and "challenge" in request.json:
        return jsonify({"challenge": request.json["challenge"]}), 200

    # Проверка подлинности запроса
    if request.headers.get("X-Avito-Signature") != AVITO_WEBHOOK_SECRET:
        return "Invalid signature", 403

    data = request.json
    logger.info(f"Webhook received: {data}")

    # Обработка нового сообщения
    if data["event"] == "message/new":
        user_id = data["user"]["id"]
        chat_id = data["chat"]["id"]
        user_message = data["message"]["text"]

        session = get_user_session(user_id)
        
        # Добавляем сообщение пользователя в историю
        session["history"].append({"role": "user", "content": user_message})

        # Формируем полный контекст для модели
        # Включаем system-промпт и всю историю
        full_messages = session["history"][-10:]  # Отправляем последние 10 сообщений для контекста

        # Запрашиваем ответ у YandexGPT
        response_text = ask_yandexgpt(full_messages)

        # Добавляем ответ ассистента в историю
        session["history"].append({"role": "assistant", "content": response_text})

        # Обновляем шаг на основе ответа (упрощённо, модель сама ведёт диалог по промпту)
        # Продвинутый вариант: использовать function calling для явного переключения шагов
        logger.info(f"User {user_id}: step={session['step']}, message={user_message}")

        # Отправляем ответ в Авито
        send_message_to_avito(chat_id, response_text)

        return jsonify({"status": "ok"}), 200

    return jsonify({"status": "ignored"}), 200

